export function expertYoutubeUrl(query) {
  return `https://www.youtube.com/results?search_query=${encodeURIComponent(
    `how to ${query}`,
  )}`;
}

// Standardized exercise shape:
// { name: { en, es }, sets, reps, rest, note?, youtube }
function ex(en, es, sets, reps, rest, note) {
  return {
    name: { en, es: es || en },
    sets,
    reps,
    rest,
    note: note || null,
    youtube: en,
  };
}

